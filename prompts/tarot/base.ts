/**
 * 根据牌阵类型返回对应的基础规则
 */
export function getBaseRules(spreadType: string): string {
  switch (spreadType) {
    case "three":
      return TAROT_THREE_CARD_RULES;
    case "celtic":
      return CELTIC_RULES;
    case "single":
    default:
      return TAROT_BASE_RULES;
  }
}

